#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	char	checker;

	checker = *str;
	while (checker == '\n')
	{
		ft_putchar(checker);
		str++;
	}
}
